        // script for timer
        let p_day = document.getElementById('day');
        let p_hour = document.getElementById('hour');
        let p_minute = document.getElementById('minute');
        let p_second = document.getElementById('second');

        function countDown() {
            let secondsNow = new Date();
            let targetSecond = new Date(2023, 10, 15, 9, 0, 0);
            let secondsDifference = targetSecond - secondsNow;

            if (secondsDifference < 0) {
                secondsDifference = 0;
            }

            let dayDifference = Math.floor(secondsDifference / 60 / 60 / 24 / 1000);
            p_day.innerHTML = dayDifference;

            let hourDifference = secondsDifference - (dayDifference * 60 * 60 * 24 * 1000);
            hourDifference = Math.floor(hourDifference / 60 / 60 / 1000);
            p_hour.innerHTML = hourDifference;

            let minuteDifference = secondsDifference - (dayDifference * 60 * 60 * 24 * 1000);
            minuteDifference = minuteDifference - (hourDifference * 60 * 60 * 1000);
            minuteDifference = Math.floor(minuteDifference / 60 / 1000);
            p_minute.innerHTML = minuteDifference;

            let second = secondsDifference - (dayDifference * 60 * 60 * 24 * 1000);
            second = second - (hourDifference * 60 * 60 * 1000);
            second = second - (minuteDifference * 60 * 1000);
            second = Math.floor(second / 1000);
            p_second.innerHTML = second;

        }

        setInterval(countDown, 1000);
    </script>

